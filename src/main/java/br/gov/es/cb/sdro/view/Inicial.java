    public int getIdTipoLogin(String tipoLogin){
        for (Map.Entry<String, Integer> entry : mapComboTipoLogin.entrySet()) {
            String key = entry.getKey();
            Integer idTipoLogin = entry.getValue();
            if(tipoLogin.equals(key)){
                return idTipoLogin;
            }
        }
        return 0;
    }

